public class Third {
    private Long uid;
    private String username;

    public Third(Long uid, String username) {
        this.uid = uid;
        this.username = username;
    }

    public Long getUid() {
        return uid;
    }

    public void setUid(Long uid) {
        this.uid = uid;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
